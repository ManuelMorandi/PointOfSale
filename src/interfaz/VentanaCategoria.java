    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String descripcion = txtDescripcion.getText();
        int prioridad = (int)spPrioridad.getValue();
        String detalles = txtDetalles.getText();
        if(descripcion.equals("") || detalles.equals("")){
            JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if(!modelo.existeCategoria(descripcion)){
            Categoria categoria = new Categoria(descripcion,detalles,prioridad);
            modelo.agregarCategoria(categoria);
            JOptionPane.showMessageDialog(this, "Categoría registrada", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            txtDescripcion.setText("");
            spPrioridad.setValue(1);
            txtDetalles.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this, "Categoria ya registrada", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

