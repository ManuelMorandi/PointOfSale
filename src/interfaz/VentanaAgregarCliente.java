    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombre = txtNombre.getText();
        String direccion = txtDireccion.getText();
        String telefono = txtTelefono.getText();
        if(nombre.equals("") || direccion.equals("") || telefono.equals("")){
            JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if(!modelo.existeCliente(nombre)){
            Cliente cliente = new Cliente(nombre,direccion,telefono);
            modelo.agregarCliente(cliente);
            JOptionPane.showMessageDialog(this, "Cliente registrado", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            txtNombre.setText("");
            txtDireccion.setText("");
            txtTelefono.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this, "Cliente ya registrado", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

