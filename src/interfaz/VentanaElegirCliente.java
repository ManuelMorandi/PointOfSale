    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        lstClientes.setListData(modelo.getListaClientes().toArray());
        txtBuscar.setText("");
    }//GEN-LAST:event_btnResetActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nombre = txtBuscar.getText();
        if(nombre.equals("")){
            lstClientes.setListData(modelo.getListaClientes().toArray());
        }
        else{
            ArrayList<Cliente> lista = modelo.buscarClientes(nombre);
            lstClientes.setListData(lista.toArray());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        Cliente cliente = (Cliente)lstClientes.getSelectedValue();
        modelo.setSeleccionado(cliente);
        dispose();
    }//GEN-LAST:event_btnSeleccionarActionPerformed

